// Blog Carousel Functionality
function initBlogCarousel() {
  const carouselContainer = document.querySelector('.blog-carousel-container');
  const carouselTrack = document.querySelector('.blog-carousel-track');
  const leftArrow = document.querySelector('.blog-carousel-arrow-left');
  const rightArrow = document.querySelector('.blog-carousel-arrow-right');

  if (!carouselContainer || !carouselTrack || !leftArrow || !rightArrow) return;

  // Update edge fade visibility
  function updateEdgeFades() {
    const scrollLeft = carouselTrack.scrollLeft;
    const maxScroll = carouselTrack.scrollWidth - carouselTrack.clientWidth;

    if (scrollLeft <= 0) {
      carouselContainer.classList.add('at-start');
    } else {
      carouselContainer.classList.remove('at-start');
    }

    if (scrollLeft >= maxScroll - 1) {
      carouselContainer.classList.add('at-end');
    } else {
      carouselContainer.classList.remove('at-end');
    }

    // Update arrow disabled state
    leftArrow.disabled = scrollLeft <= 0;
    rightArrow.disabled = scrollLeft >= maxScroll - 1;
  }

  // Scroll carousel
  function scrollCarousel(direction) {
    const scrollAmount = carouselTrack.clientWidth * 0.8;
    const currentScroll = carouselTrack.scrollLeft;
    const targetScroll = direction === 'left' 
      ? Math.max(0, currentScroll - scrollAmount)
      : Math.min(carouselTrack.scrollWidth - carouselTrack.clientWidth, currentScroll + scrollAmount);

    carouselTrack.scrollTo({
      left: targetScroll,
      behavior: 'smooth'
    });
  }

  // Arrow button event listeners
  leftArrow.addEventListener('click', () => scrollCarousel('left'));
  rightArrow.addEventListener('click', () => scrollCarousel('right'));

  // Scroll event listener
  carouselTrack.addEventListener('scroll', updateEdgeFades);

  // Mouse drag functionality
  let isDown = false;
  let startX;
  let scrollLeftStart;

  carouselTrack.addEventListener('mousedown', (e) => {
    isDown = true;
    carouselTrack.style.cursor = 'grabbing';
    startX = e.pageX - carouselTrack.offsetLeft;
    scrollLeftStart = carouselTrack.scrollLeft;
  });

  carouselTrack.addEventListener('mouseleave', () => {
    isDown = false;
    carouselTrack.style.cursor = 'grab';
  });

  carouselTrack.addEventListener('mouseup', () => {
    isDown = false;
    carouselTrack.style.cursor = 'grab';
  });

  carouselTrack.addEventListener('mousemove', (e) => {
    if (!isDown) return;
    e.preventDefault();
    const x = e.pageX - carouselTrack.offsetLeft;
    const walk = (x - startX) * 2;
    carouselTrack.scrollLeft = scrollLeftStart - walk;
  });

  // Initialize edge fades
  updateEdgeFades();

  // Update on window resize
  window.addEventListener('resize', updateEdgeFades);
}


